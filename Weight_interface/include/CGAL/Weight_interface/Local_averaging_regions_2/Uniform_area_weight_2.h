#ifndef CGAL_GENERALIZED_UNIFORM_AREA_WEIGHT_2_H
#define CGAL_GENERALIZED_UNIFORM_AREA_WEIGHT_2_H

// #include <CGAL/license/Weight_interface.h>

// Internal includes.
#include <CGAL/Weight_interface/internal/utils_2.h>

namespace CGAL {
namespace Generalized_weights {

  /*!
    \ingroup PkgWeightInterfaceRef2DAverage

    \brief 2D uniform area weight.

    This weight always returns 1.

    \tparam GeomTraits
    must be a model of `AnalyticTraits_2`.

    \cgalModels `HalfWeight_2`
  */
  template<typename GeomTraits>
  class Uniform_area_weight_2 {

  public:

    /// \name Types
    /// @{

    /// \cond SKIP_IN_MANUAL
    using GT = GeomTraits;
    /// \endcond

    /// Number type.
    typedef typename GeomTraits::FT FT;

    /// 2D point type.
    typedef typename GeomTraits::Point_2 Point_2;

    /// 3D point type.
    typedef typename GeomTraits::Point_3 Point_3;

    /// @}

    /// \name Initialization
    /// @{

    /*!
      \brief initializes all internal data structures.

      \param traits
      An instance of `GeomTraits`. The default initialization is provided.
    */
    Uniform_area_weight_2(
      const GeomTraits traits = GeomTraits()) :
    m_traits(traits)
    { }

    /// @}

    /// \name Access
    /// @{

    /*!
      \brief computes 2D uniform area weight.
    */
    const FT operator()(
      const Point_2&,
      const Point_2&,
      const Point_2&) const {

      return weight();
    }

    /*!
      \brief computes 2D uniform area weight.
    */
    const FT operator()(
      const Point_3&,
      const Point_3&,
      const Point_3&) const {

      return weight();
    }

    /// @}

  private:
    const GeomTraits m_traits;

    const FT weight() const {
      return FT(1);
    }
  };

} // namespace Generalized_weights
} // namespace CGAL

#endif // CGAL_GENERALIZED_UNIFORM_AREA_WEIGHT_2_H
